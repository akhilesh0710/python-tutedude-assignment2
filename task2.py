counter = 50  # Set the upper limit for the range
sum = 0  # Initialize sum to 0 before the loop

# Loop through numbers from 1 to counter (inclusive)
for i in range(1, counter + 1):
    sum = sum + i  # Add the current number to sum

# Print the final result
print(f"The sum of numbers from 1 to {counter} is: {sum}")
